    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        Connection con=new DBKoneksi().connect();
        Statement st;
        ResultSet rs;
        try{  
                
                String namafile= "src/Laporan/ReportDataMahasiswa.jasper"; 
                String sql="select * from mahasiswa where nim=1209412";
                st=con.createStatement();
                rs=st.executeQuery(sql);
                HashMap param = new HashMap();                
                param.put("parameter1","tes"); 
                JRResultSetDataSource rsd=new JRResultSetDataSource(rs);
                System.out.println("tampilkan laporan");
                JasperPrint jp = JasperFillManager.fillReport(namafile,param,rsd);
                JasperViewer.viewReport(jp,false);
              } catch (SQLException e) {
              JOptionPane.showMessageDialog(null, "Gagal Membuka Laporan" + e,"Cetak Laporan",JOptionPane.ERROR_MESSAGE);
              } catch (JRException ex) {
            Logger.getLogger(FrmLapMahasiswa.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton4ActionPerformed

