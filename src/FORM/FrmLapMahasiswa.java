    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Connection con=new DBKoneksi().connect();
        File file=new File("src/Laporan/ReportDataMahasiswa.jrxml");
        try {
            JasperDesign jd=JRXmlLoader.load(file);
            JRDesignQuery nq=new JRDesignQuery();
            String sql="select * from mahasiswa where nim="+textfield_nim.getText();
            nq.setText(sql);
            jd.setQuery(nq);
            JasperReport js=JasperCompileManager.compileReport(jd);
            JasperPrint jp=JasperFillManager.fillReport(js,null,con);
            JasperViewer.viewReport(jp,false);
        } catch (JRException ex) {
            Logger.getLogger(FrmLapMahasiswa.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

